#comparison operators

#equal
print(3 == 3) #returns True
print(3 == 4) #returns false

#not equal
print(3 != 3) #returns false
print(3 != 4) #returns true

#greater than
print(5 > 3) #returns true
print(5 > 8) #returns false

#less than
print(3 < 2) #returns true
print(3 < 5) #returns false

#greaterthan or equal to
print(5 >= 10) #returns true
print(5 >= 5) #returns true
print(5 >= 2) #returns false

#greaterthan or equal to
print(5 <= 3) #returns true
print(5 <= 5) #returns true
print(5 <= 7) #returns false