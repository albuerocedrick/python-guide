#assignment operators

a = 5 
a += 5 #same as a = a + 5
print(a)

b = 10
b -= 5 #same as b = b - 5
print(b)

c = 3
c *= 3 #same as c = c * 3
print(c)

d = 9
d /= 3 #same as d = d / 3
print(d)

e = 10
e %= 6 #same as e = e % 
print(e)

f = 5
f //= 3 #same as f = f // 3

g = 2
g **= 3 #same as g = g ** 3 