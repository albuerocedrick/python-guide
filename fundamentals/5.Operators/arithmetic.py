#arithmetic operators
x = 10
y = 6

print(x + y) #addition
print(x - y) #subtraction
print(x * y) #multiplication
print(x / y) #division
print(x % y) #modulos (remainder when divided)
print(x ** y) #exponential(10 raise to the power of 6)
print(x // y) #floor division (normal division, but returns whole number)
